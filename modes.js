document.getElementById('sun-button').addEventListener('click', function() {
    document.body.style.backgroundColor = 'white';
    document.body.style.color = 'black';
});

document.getElementById('moon-button').addEventListener('click', function() {
    document.body.style.backgroundColor = 'black';
    document.body.style.color = 'white';
});

document.getElementById('eye-button').addEventListener('click', function() {
    document.body.style.backgroundColor = '#ebc75d';
    document.body.style.color = 'black';
});

document.getElementById('star-button').addEventListener('click', function() {
    document.body.style.backgroundColor = 'rgb(59, 59, 59)';
    document.body.style.color = 'white';
});